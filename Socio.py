class Socio:
    numIns=None
    nome=None
    end=None
    tel=None
    
    def setNumIns(self,numIns):
        self.numIns=numIns
    
    def getNumIns(self):
        return self.numIns
    
    def setNome(self,nome):
        self.nome=nome
        
    def getNome(self):
        return self.nome
    
    def setEnd(self,end):
        self.end=end
        
    def getEnd(self):
        return self.end
    
    def setTel(self,tel):
        self.tel=tel
        
    def getTel(self):
        return self.tel
