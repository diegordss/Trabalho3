class Artista:
    codA=None
    nomeA=None
    paisOrigem=None
    dataNascimento=None
    
    def setCodA(self,codA):
        self.codA=codA
    
    def getCodA(self):
        return self.codA
    
    def setnomeA(self,nomeA):
        self.nomeA=nomeA
    
    def getnomeA(self):
        return self.nomeA
    
    def setpaisOrigem(self,paisOrigem):
        self.paisOrigem=paisOrigem
    
    def getpaisOrigem(self):
        return self.paisOrigem
    
    def setdataNascimento(self,dataNascimento):
        self.dataNascimento=dataNascimento
    
    def getdataNascimento(self):
        return self.dataNascimento


